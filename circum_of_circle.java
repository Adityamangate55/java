import java.util.*;
class circum_of_circle {
    public static void main(String args[]){
        int r;
        double circum;
        final double pi=3.14;
        Scanner s=new Scanner(System.in);
        System.out.println("Enter the values");
        r=s.nextInt();
        circum=pi*r*2;
        System.out.println("The square of values is "+circum);
    }
}

